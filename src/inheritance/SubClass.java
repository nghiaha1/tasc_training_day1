package inheritance;

public class SubClass extends SuperClass{
    private String name = "Con cua Nguyen Van A";
    private int age = 20;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
